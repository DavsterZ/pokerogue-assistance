def get_effectiveness(attack_type: str, defender_types: list[str]) -> float:
    """
        Calcula el multiplicador de daño total contra uno o dos tipos.
    """

    multiplier = 1.0
    attack_type = attack_type.lower()

    # Obtenemos las efectividades del tipo atacante
    attack_relations = TYPE_EFFECTIVENESS.get(attack_type, {})

    for def_type in defender_types:
        def_type = def_type.lower()
        # Multiplicamos por la efectividad contra cada tipo del defensor
        # Si no esta en la tabla, es un 1.0
        multiplier *= attack_relations.get(def_type, 1.0)

    return multiplier